void read_reg_I2C(uint16_t dev_addr,uint8_t addr, uint8_t *data){
	HAL_I2C_Master_Transmit(&hi2c1,dev_addr,&addr,1,HAL_MAX_DELAY);
	HAL_I2C_Master_Receive(&hi2c1,dev_addr,data,sizeof(data),HAL_MAX_DELAY);
}

void write_reg_I2C(uint16_t dev_addr,uint8_t addr,uint8_t data){
	uint8_t buffer[2];
	buffer[0]=addr;
	buffer[1]=data;
	HAL_I2C_Master_Transmit(&hi2c1,dev_addr,buffer,2,HAL_MAX_DELAY);
}

void set_mag(void){
	write_reg_I2C(0x3C,0x00,0x14);
	write_reg_I2C(0x3C,0x02,0x00);
	  //write_reg_I2C(0x3C,0x01,0xE0);
}

void get_mag(void){
	HAL_I2C_Master_Transmit(&hi2c1,0x3C,&mag_reg_add,1,HAL_MAX_DELAY);
	HAL_I2C_Master_Receive(&hi2c1,0x3C,&temp,1,HAL_MAX_DELAY);
	HAL_I2C_Master_Receive(&hi2c1,0x3C,MR_Data,sizeof(MR_Data),HAL_MAX_DELAY);

	Mx = (int) (MR_Data[0] << 8) + MR_Data[1];
	My = (int) (MR_Data[2] << 8) + MR_Data[3];
	Mz = (int) (MR_Data[4] << 8) + MR_Data[5];
	dir=180* atan2((double) My,(double) Mx)/M_PI;
}

